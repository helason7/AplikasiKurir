    private void cekBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekBtnActionPerformed
        // TODO add your handling code here:
//        double berat = Double.valueOf(beratBrgText.getText());
//        paket.setBeratBarang(berat);
//        paket.setDestinasi(tujuanCombo.getSelectedItem().toString());
        try {
            Dimensi ukuran = new Dimensi();
            ukuran.setPanjang(Double.valueOf(pjgTxt.getText()));
            ukuran.setLebar(Double.valueOf(lbrTxt.getText()));
            ukuran.setTinggi(Double.valueOf(tinggiTxt.getText()));
            PaketModel pm = new PaketModel();
            pm.setDestinasi(tujuanCombo.getSelectedItem().toString());
                pm.setAsuransi(true);
            if (asuransiCek.isSelected()) {
                pm.setAsuransi(true);
            } else {
                pm.setAsuransi(false);
            }
            pm.setHargaBarang(Double.parseDouble(hargaBrgText.getText()));
            pm.setBeratBarang(Double.valueOf(beratBrgText.getText()));
            pm.setJenisLayanan(layananCombo.getSelectedItem().toString());
            pm.setUkuran(ukuran);
            pm.setNoPaket("1001");
            pm.setTglKirim(new Date());
            
            OngkirDAO ongkir = new OngkirDAO();
            
            
            
            ongkitTxt.setText(String.valueOf(ongkir.hitungOngkir(pm)));
            asuransiTxt.setText(String.valueOf(ongkir.hitungAsuransi(pm)));
            totOngkirTxt.setText(String.valueOf(ongkir.totalOngkir(pm)));

        } catch (Exception e) {
                e.printStackTrace();
        }
    }//GEN-LAST:event_cekBtnActionPerformed

