    public void isiKotaProv(){
        List<KotaProvinsi> list = ProvinsiDAO.getKotaProvinsi();
        for (KotaProvinsi object : list) {
            kotaPengirimCombo.addItem(object.getKota());
            provPengirimCombo.addItem(object.getProvinsi());
            kotaPenerimaCombo.addItem(object.getKota());
            provPenerimaCombo.addItem(object.getProvinsi());
            
        }
    }

